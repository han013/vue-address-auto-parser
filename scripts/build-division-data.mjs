import { execSync } from "node:child_process";
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";

const root = resolve(process.cwd());
const dbPath = resolve(root, "data/raw/data.sqlite");
const outPath = resolve(root, "src/data/default-division-tree.js");

function query(sql) {
  const command = `sqlite3 -json "${dbPath}" "${sql}"`;
  const raw = execSync(command, { encoding: "utf8" });
  return JSON.parse(raw || "[]");
}

const provinces = query("select code,name from province order by code;");
const cities = query("select code,name,provinceCode from city order by code;");
const areas = query("select code,name,cityCode from area order by code;");

const cityMap = new Map();
for (const c of cities) {
  if (!cityMap.has(c.provinceCode)) cityMap.set(c.provinceCode, []);
  cityMap.get(c.provinceCode).push({
    code: c.code,
    name: c.name,
    aliases: [],
    districts: [],
  });
}

const cityByCode = new Map();
for (const list of cityMap.values()) {
  list.forEach((c) => cityByCode.set(c.code, c));
}

for (const a of areas) {
  const city = cityByCode.get(a.cityCode);
  if (!city) continue;
  city.districts.push({
    code: a.code,
    name: a.name,
    aliases: [],
  });
}

const divisionTree = provinces.map((p) => ({
  code: p.code,
  name: p.name,
  aliases: [],
  cities: cityMap.get(p.code) || [],
}));

const header = `/* eslint-disable */
// Auto-generated by scripts/build-division-data.mjs
// Source: data/raw/data.sqlite (province/city/area)
// Do not edit manually.
`;

const code = `${header}
export const divisionDataMeta = ${JSON.stringify(
  {
    source: "data/raw/data.sqlite",
    level: "province-city-area",
    provinceCount: provinces.length,
    cityCount: cities.length,
    districtCount: areas.length,
    generatedAt: new Date().toISOString(),
  },
  null,
  2
)};

export const defaultDivisionTree = ${JSON.stringify(divisionTree, null, 2)};

export default defaultDivisionTree;
`;

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, code, "utf8");

console.log(`Generated: ${outPath}`);
console.log(
  `Counts => provinces: ${provinces.length}, cities: ${cities.length}, districts: ${areas.length}`
);
