import { execSync } from "node:child_process";
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";

const root = resolve(process.cwd());
const dbPath = resolve(root, "data/raw/data.sqlite");
const outPath = resolve(root, "src/data/default-townships.js");

function query(sql) {
  const command = `sqlite3 -json "${dbPath}" "${sql}"`;
  const raw = execSync(command, { encoding: "utf8", maxBuffer: 1024 * 1024 * 256 });
  return JSON.parse(raw || "[]");
}

const rows = query(`
  select
    s.code as code,
    s.name as name,
    p.name as province,
    c.name as city,
    a.name as district
  from street s
  left join province p on p.code = s.provinceCode
  left join city c on c.code = s.cityCode
  left join area a on a.code = s.areaCode
  where s.name is not null and s.name != ''
  order by s.code;
`);

const townships = rows.map((row) => ({
  code: row.code,
  name: row.name,
  aliases: [],
  province: row.province || "",
  city: row.city || "",
  district: row.district || "",
  type: "乡镇",
}));

const code = `/* eslint-disable */
// Auto-generated by scripts/build-townships-data.mjs
// Source: data/raw/data.sqlite (street table)
// Do not edit manually.

export const townshipDataMeta = ${JSON.stringify(
  {
    source: "data/raw/data.sqlite",
    table: "street",
    totalTownships: townships.length,
    generatedAt: new Date().toISOString(),
  },
  null,
  2
)};

export const defaultTownships = ${JSON.stringify(townships, null, 2)};

export default defaultTownships;
`;

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, code, "utf8");

console.log(`Generated: ${outPath}`);
console.log(`Townships: ${townships.length}`);
