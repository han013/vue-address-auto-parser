import { execSync } from "node:child_process";
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";

const root = resolve(process.cwd());
const dbPath = resolve(root, "data/raw/data.sqlite");
const outPath = resolve(root, "src/data/default-landmarks.js");

function query(sql) {
  const command = `sqlite3 -json "${dbPath}" "${sql}"`;
  const raw = execSync(command, { encoding: "utf8" });
  return JSON.parse(raw || "[]");
}

const rows = query(`
  select
    s.code as code,
    s.name as name,
    'street' as level,
    p.name as province,
    c.name as city,
    a.name as district
  from street s
  left join province p on p.code = s.provinceCode
  left join city c on c.code = s.cityCode
  left join area a on a.code = s.areaCode
  where s.name like '%口岸'
  union all
  select
    v.code as code,
    v.name as name,
    'village' as level,
    p.name as province,
    c.name as city,
    a.name as district
  from village v
  left join province p on p.code = v.provinceCode
  left join city c on c.code = v.cityCode
  left join area a on a.code = v.areaCode
  where v.name like '%口岸'
  order by name, code;
`);

const byName = new Map();
for (const row of rows) {
  if (!row.name) continue;
  if (!byName.has(row.name)) byName.set(row.name, []);
  byName.get(row.name).push(row);
}

const landmarks = [];
for (const [name, list] of byName.entries()) {
  // 同名记录优先选择街道层级
  const chosen = list.find((x) => x.level === "street") || list[0];
  landmarks.push({
    name,
    aliases: [name.replace(/口岸$/, "")].filter(Boolean),
    province: chosen.province || "",
    city: chosen.city || "",
    district: chosen.district || "",
    type: "口岸",
    sourceLevel: chosen.level,
    sourceCode: chosen.code,
  });
}

landmarks.sort((a, b) => a.name.localeCompare(b.name, "zh-CN"));

const code = `/* eslint-disable */
// Auto-generated by scripts/build-landmarks-data.mjs
// Source: data/raw/data.sqlite (street/village where name like '%口岸')
// Do not edit manually.

export const landmarkDataMeta = ${JSON.stringify(
  {
    source: "data/raw/data.sqlite",
    keyword: "name like '%口岸'",
    totalRows: rows.length,
    uniqueLandmarks: landmarks.length,
    generatedAt: new Date().toISOString(),
  },
  null,
  2
)};

export const defaultLandmarks = ${JSON.stringify(landmarks, null, 2)};

export default defaultLandmarks;
`;

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, code, "utf8");

console.log(`Generated: ${outPath}`);
console.log(`Rows: ${rows.length}, unique landmarks: ${landmarks.length}`);
